/**
 * @brief Simple addition function
 * 
 * @param a 
 * @param b 
 * @return int 
 */
int sum(int a,int b )
{
    return a+b;
}

/**
 * @brief Simple substraction function
 * 
 * @param a 
 * @param b 
 * @return int 
 */
int sub(int a, int b )
{
    return a - b;
}